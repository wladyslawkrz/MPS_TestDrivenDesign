namespace ClassLibrary
{
    public class Calculate
    {
        public int Add(int v1, int v2)
        {
            return v1 + v2;
        }

        public int Substract(int v1, int v2)
        {
            return v1 - v2;
        }

        public int Multiply(int v1, int v2)
        {
            return v1 * v2;
        }

    }
}